#include "libft.h"

void	ft_putnbr(int n)
{
	long	l;

	l = n;
	if (l < 0)
	{
		ft_putchar('-');
		l = -l;
	}
	if (l > 9)
		ft_putnbr(l / 10);
	ft_putchar(l % 10 + '0');
}
