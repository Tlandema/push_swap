char	*ft_strrev(char *str)
{
	int		i;
	int		s;
	char	temp;

	s = 0;
	i = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (i > s)
	{
		temp = str[i];
		str[i] = str[s];
		str[s] = temp;
		i--;
		s++;
	}
	return (str);
}
